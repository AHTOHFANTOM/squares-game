package com.task1.console;

/**
 * Перечисление CommandType определяет все возможные типы команд,
 * которые может вводить пользователь или система.
 */
public enum CommandType {
    /** Команда для начала новой игры. Формат: "GAME N, U1, U2" */
    GAME,

    /** Команда для выполнения хода. Формат: "MOVE X, Y" или "MOVE X Y" */
    MOVE,

    /** Команда для отображения справки по доступным командам */
    HELP,

    /** Команда для завершения работы программы */
    EXIT,

    /** Неизвестная или некорректная команда */
    UNKNOWN
}
